package com.mkw.core.property;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;

@Component
public class ConfigProperty {
	
	@Value("${domain}")
	private String domain;
	
	@Value("${domain.merchant.name}")
	private String domainMerchantName;
	
	@Value("${domain.merchant.base}")
	private String domainMerchantBase;
	
	@Value("${domain.client.name}")
	private String domainClientName;
	
	@Value("${domain.client.base}")
	private String domainClientBase;
	
	@Value("${domain.colligate.name}")
	private String domainColligateName;
	
	@Value("${domain.colligate.base}")
	private String domainColligateBase;
	
	@Value("${upload.damain.name}")
	private String uploadDamainName;
	
	@Value("${upload.damain.path}")
	private String uploadDamainPath;

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public String getDomainMerchantName() {
		return domainMerchantName;
	}

	public void setDomainMerchantName(String domainMerchantName) {
		this.domainMerchantName = domainMerchantName;
	}

	public String getDomainMerchantBase() {
		return domainMerchantBase;
	}

	public void setDomainMerchantBase(String domainMerchantBase) {
		this.domainMerchantBase = domainMerchantBase;
	}

	public String getDomainClientName() {
		return domainClientName;
	}

	public void setDomainClientName(String domainClientName) {
		this.domainClientName = domainClientName;
	}

	public String getDomainClientBase() {
		return domainClientBase;
	}

	public void setDomainClientBase(String domainClientBase) {
		this.domainClientBase = domainClientBase;
	}

	public String getDomainColligateName() {
		return domainColligateName;
	}

	public void setDomainColligateName(String domainColligateName) {
		this.domainColligateName = domainColligateName;
	}

	public String getDomainColligateBase() {
		return domainColligateBase;
	}

	public void setDomainColligateBase(String domainColligateBase) {
		this.domainColligateBase = domainColligateBase;
	}

	public String getUploadDamainName() {
		return uploadDamainName;
	}

	public void setUploadDamainName(String uploadDamainName) {
		this.uploadDamainName = uploadDamainName;
	}

	public String getUploadDamainPath() {
		return uploadDamainPath;
	}

	public void setUploadDamainPath(String uploadDamainPath) {
		this.uploadDamainPath = uploadDamainPath;
	}
}
